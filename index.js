function rnd(max) {
  return Math.floor(Math.random() * Math.floor(max));
}

function rndArray(array) {
  return array[rnd(array.length)];
}

// Generate an easy-read-id using an optional list, seperator and max number
exports.generate = function(list, maxRandom, seperator) {
  let componentsList = list || ['adjective', 'animal', 'number'];
  let max = maxRandom || 100;
  let joinSeperator = seperator || '-';
  let readableID = [];
  let elements = {
    adjective: rndArray(exports.adjective),
    animal: rndArray(exports.animal),
    nautical: rndArray(exports.nautical),
    color: rndArray(exports.color),
    noun: rndArray(exports.noun),
    action: rndArray(exports.action),
    number: rnd(max)
  }

  for (var i = 0; i < componentsList.length; i++) {
    readableID.push(elements[componentsList[i]]);
  }

  return readableID.join(joinSeperator);
}


// Calculate id space available
exports.getIDSpace = function(list, maxRandom) {
  let componentsList = list || ['adjective', 'animal', 'number'];
  let max = maxRandom || 100;
  let space = 1;

  for (let j = 0; j < componentsList.length; j++) {
    if(componentsList[j] === 'number') {
      space = space * max;
    } else {
      space = exports[componentsList[j]].length * space;
    }
  }
  return space;
}

